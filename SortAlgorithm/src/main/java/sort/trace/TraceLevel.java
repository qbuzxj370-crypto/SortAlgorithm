package main.java.sort.trace;

/*
 * 기능: 정렬 과정 추적(Trace)의 출력 범위를 정의하는 열거형.
 * 
 * 요구사항
 * 	정렬 과정 이벤트의 출력 수준을 단계적으로 구분할 수 있어야 한다.
 * 	출력 없음, 패스 단위, 교환 단위, 비교 단위 등의 레벨을 포함해야 한다.
 * 	ConsoleTraceListener 등 출력 구현체에서 이 값을 기준으로 동작을 제어해야 한다.
 * 	레벨 간에는 명확한 포함 관계가 정의되어야 한다.
 * 
 * 제약
 * 	정렬 알고리즘 로직과 직접 결합되지 않는다.
 * 	출력 정책만을 정의하며, 실제 출력 동작을 포함하지 않는다.
 */

public enum TraceLevel {
	NONE,      // 출력 없음
    PASS,      // 패스 종료 시점만 출력
    SWAP,      // 교환 발생 시 출력
    COMPARE    // 비교 단계까지 출력
}
